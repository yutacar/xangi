/**
 * LINE 専用の応答ルール。
 *
 * LINE のトーク画面はマークダウンレンダリングを行わないため、
 * `**bold**` / `*italic*` / `~~strike~~` / `` `code` `` 等の強調記法が
 * そのまま文字として表示されてしまう。視認性を保つため、LINE 経由の
 * 応答ではこれらの記号を避け、絵文字 / 改行 / 全角記号で表現する。
 *
 * Discord/Slack 用の `xangi-cmd discord_*` / `xangi-cmd slack_*` 系コマンドや
 * `MEDIA:` ファイル送信機構は LINE 経由では使えないため、本ファイルでは
 * 案内しない (xangi-commands-chat-platform.ts や discord.ts は注入されない構成)。
 */
export const XANGI_COMMANDS_LINE = `## LINEプラットフォームでの応答ルール

LINEのトーク画面ではMarkdown記法はレンダリングされません（記号がそのまま文字として表示されます）。以下のルールに従って応答してください。

### 使わない記号
- 強調: \`**bold**\` \`*italic*\` \`__under__\` \`_italic_\` \`~~strike~~\`（記号が文字として残ります）
- 見出し: \`#\` \`##\` \`###\`（先頭の \`#\` が文字として残ります）
- インラインコード: バッククォートで囲む書き方（バッククォートが文字として残ります）
- コードブロック: 三連バッククォート（同上）
- リスト記号: 行頭の \`-\` \`*\` \`+\`（記号が文字として残ります）
- 引用: 行頭の \`>\`（記号が文字として残ります）

### 代わりに使える表現
- 強調したいとき: 絵文字（🎉 ✨ 💡 ❤️ 😊 など）、改行、カッコ「」、句読点で区切る
- リスト: 行頭に「・」（中黒）か「1.」「2.」のような装飾文字。\`-\` や \`*\` は使わない
- 区切り: 空行、または「―――」「・・・」「＝＝＝」など
- リンク: URLをそのまま書く（LINEが自動でクリック可能にします）
- コード片: そのままテキストとして書くか、必要なら「【】」で囲む

### 文章の長さ
- LINEテキストメッセージは5000字制限ですが、それ以下でも1メッセージは画面で読みやすい長さに収めてください（目安: 200〜800字）
- 長くなりそうなら段落を分けて、読みやすく改行を入れる

### ファイル送信
- LINE経由ではファイル送信（MEDIA:形式）は非対応です。応答はテキストのみで完結させてください
- 画像や動画を案内する必要がある場合はURLで示す（ユーザがブラウザで開けます）

### 例
誤: \`**ねぇ、聞いて！**\` → 画面に「**ねぇ、聞いて！**」とそのまま表示されます
正: \`ねぇ、聞いて！✨\` → 絵文字で柔らかく強調

誤: \`- りんご\\n- みかん\\n- ばなな\`
正: \`・りんご\\n・みかん\\n・ばなな\`
`;
