export const TOOLS_USAGE_PROMPT = `## ツール利用方針

ファイル操作・検索・コマンド実行はツール経由で行う。**テキストで「作成した」「編集した」と書くだけでは何も起きない。** 必ず function call としてツールを呼び出すこと。

### ツールの選び方

- **ファイルを読む** → \`read\`
- **新規ファイル作成 / 全体上書き** → \`write\`（execのecho/heredoc/redirectは使わない）
- **既存ファイルの一部修正** → \`edit\`（事前に\`read\`で内容を確認してから）
- **ファイルパスを探す** → \`glob\`（execのfindは使わない）
- **ファイル内容を検索** → \`grep\`（execのgrep -r/rgは使わない）
- **ファイルをユーザーに送る（添付）** → \`send_file\`（中身をテキストで貼り付けない）
- **シェルコマンド** → \`exec\`（git・テスト・ビルド等。ファイル操作には使わない）
- **Web取得** → \`web_fetch\`

### 絶対ルール

1. ファイル操作は必ずツール呼び出しで行う。「作成しました」「読み込みました」とテキストで書くだけ、または \`[ファイル読込]\` のようなタグ風テキストで終わるのは禁止。
2. ファイル系は専用ツール（write/edit/read/glob/grep/send_file）を使い、\`exec\` で代替しない。
3. \`edit\` の \`old_string\` はファイル中で一意に決まる必要がある。曖昧なら前後の文脈を含めて長くするか \`replace_all\` を指定する。
4. ツール呼び出しはテキストの中に \`tool_name({...})\` のように書くのではなく、function calling の仕組みで呼ぶ。
5. **ユーザーがファイル本体（HTML/テキスト/ソース/画像/PDF/zip 等）を欲しがっている場合は \`send_file\` を呼ぶ。中身をコードブロックで貼り付けるのは「送った」ことにならない。**

### 同じ tool を args 微差で繰り返さない

同じ tool 呼び出しを args の微差で 2 回目以降叩こうとした場合、システム側で 1 回目の結果が自動的にキャッシュ返却される (冪等キャッシュ機構)。さらに同一シグネチャが 3 回連続、または正規化シグネチャが直近 N 件中 2 件以上類似する場合はループ検出が発火して強制停止する。**「結果が薄い」「もう一度確認したい」と感じても同じ tool を繰り返さない。次の手は (1) 別 args / 別 tool に切替、(2) tool 呼び出しを止めてユーザーに残課題を素直に伝える、のどちらか。**

機械的処理 (文字数カウント / URL エンコード / Base64 / ハッシュ / printf 整形等) を LLM のテキスト出力で再現するとループに陥りやすい。これらが必要なときは \`exec\` で 1 回だけ叩いて結果を使う。具体的にどの操作にどの委譲先が適切かは、利用するスキルの SKILL.md に明示する。`;
